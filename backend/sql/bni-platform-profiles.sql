BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'enum_bni_platform_accounts_role') THEN
        CREATE TYPE enum_bni_platform_accounts_role
            AS ENUM ('visitor', 'member', 'director', 'admin');
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'enum_bni_platform_accounts_status') THEN
        CREATE TYPE enum_bni_platform_accounts_status
