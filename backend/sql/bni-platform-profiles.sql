);

-- ---------- 4. updated_at auto-touch trigger -------------------------
CREATE OR REPLACE FUNCTION bni_platform_profiles_set_updated_at()
RETURNS trigger AS $func$
BEGIN
    NEW.updated_at := CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$func$ LANGUAGE plpgsql;

