--   * `datetime`            → `timestamptz`
--   * `ON UPDATE CURRENT_TIMESTAMP` → `BEFORE UPDATE` trigger (Postgres has
--     no row-level ON UPDATE clause).
--   * Long string values use dollar-quoting `$xx$ … $xx$` so we don't have
--     to escape `'`, `"`, `\` or newlines inside JSON / Mongolian text.
--
-- Safe to run repeatedly: every CREATE uses IF NOT EXISTS, every INSERT
-- uses ON CONFLICT DO UPDATE.
--
-- HOW TO USE in pgAdmin
--   1. Right-click the **database** node (NOT a table) → Query Tool.
--      (Right-clicking a table that this script later drops/creates is
--       what produces the "specified object could not be found" error.)
--   2. Paste the whole file → ▶ Execute.
--   3. Verify:  SELECT account_id, display_name, company_name
